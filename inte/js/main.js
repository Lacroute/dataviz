$(document).ready(function(){

$('#openMenu').on('click', function(){

	$('#content').toggleClass('on');
});

$('#openNote').on('click', function(){

	$('#note').toggleClass('on');
});

$('#closeNote').on('click', function(){

	$('#note').toggleClass('on');
});

$('#menu').on('click', function(){
	$('#content').toggleClass('on');


});
	
});




